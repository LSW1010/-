// Helper to format string dates beautifully
function generateFormattedDate(index: number, total: number): string {
  const startDate = new Date('2024-01-01').getTime();
  const endDate = new Date('2026-06-13').getTime(); // App current limit simulation
  
  // Distribute dates incrementally from oldest to newest (index 0 is oldest, last is current)
  const range = endDate - startDate;
  // Intentionally add some random scattering jitter to make dates look natural but strictly chronologically ordered on average
  const fraction = index / total;
  const jitter = (Math.random() - 0.5) * (range / total) * 0.8;
  const targetTime = startDate + fraction * range + jitter;
  
  // Safe bounds guard
  const finalTime = Math.min(Math.max(targetTime, startDate), endDate);
  const dateObj = new Date(finalTime);
  
  const yyyy = dateObj.getFullYear();
  const mm = String(dateObj.getMonth() + 1).padStart(2, '0');
  const dd = String(dateObj.getDate()).padStart(2, '0');
  
  return `${yyyy}-${mm}-${dd}`;
}

